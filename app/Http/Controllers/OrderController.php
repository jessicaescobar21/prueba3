     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $orders = Order::select(
            "orders.ord_num",
            "orders.ord_amount",
            "orders.ord_date",
            "orders.ord_description",
            "customers.cust_name",
            "agents.agent_name",
            "agents.working_area",
        )
        ->join("customers", "customers.cust_code", "=", "orders.ord_num")
        ->join("agents", "agents.agent_code", "=", "orders.ord_num")
        ->paginate(2);
        return response()->json($orders);
    }

